package OOPSJAVA;

public class classes {
    public int x = 5;
    static int y = 3;

    public static void main(String[] args) {
        classes myg = new classes();
        System.out.println(myg.x);
        System.out.println(myg.y);
        System.out.println(x);
        System.out.println(y);

    }

}
